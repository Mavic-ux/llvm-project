#ifndef LLVM_LIB_TARGET_ZEN_ZENSUBTARGET_H
#define LLVM_LIB_TARGET_ZEN_ZENSUBTARGET_H

#include "ZENFrameLowering.h"
#include "ZENISelLowering.h"
#include "ZENInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "ZENGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class ZENSubtarget : public ZENGenSubtargetInfo {
  virtual void anchor();

  ZENRegisterInfo RegInfo;
  ZENInstrInfo InstrInfo;
  ZENFrameLowering FrameLowering;
  ZENTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  ZENSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const ZENInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const ZENFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const ZENTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const ZENRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_ZEN_ZENSUBTARGET_H